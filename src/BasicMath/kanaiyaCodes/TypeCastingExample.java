package BasicMath.kanaiyaCodes;

public class TypeCastingExample {
    public static void main(String[] args) {
        double myDouble = 9.78;
        int myInt = (int) myDouble;  // Explicit type casting from double to int

        System.out.println("Double value: " + myDouble); // Output: Double value: 9.78
        System.out.println("Integer value: " + myInt);   // Output: Integer value: 9
    }

}
